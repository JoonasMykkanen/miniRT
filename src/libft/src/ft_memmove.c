#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	if (d > s)
		d = ft_memrcpy(d, s, n);
	else
		d = ft_memcpy(d, s, n);
	return (d);
}
